package com.mycompany.model.DTO;

import javax.xml.bind.annotation.XmlElement;

/**
 *
 * @author ramen
 */
public class Hero {
    
    int id;
    String name;

    public Hero() {
    }

    public Hero(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @XmlElement
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
